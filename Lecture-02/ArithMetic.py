x = 17
y = 4

print('x + y =', x + y)  
# Addition
print('x - y =', x - y) 
 # Subtraction
print('x * y =', x * y)
print('x / y =', x / y)  # Division
print('x % y =', x % y)  # Modulus
print('x // y =', x // y)  # Floor Division

print('x ** y =', x ** y)  # Exponentiation

print('5+18//2-25%4 =', 5 + 18 // 2 - 25 % 4)  # Operator Precedence